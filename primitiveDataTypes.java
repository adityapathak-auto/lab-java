public class primitiveDataTypes {

    public static void main(String args[]){

        //Primitive Data Types in java --------->//


        // whole numbers data types are byte, short, int , long

        byte data = 45;
        System.out.println(data);

        short data1 = 2227;
        System.out.println(data1);

        int data2 = 38738478;
        System.out.println(data2);

        long data3 = 796379634l;  // we have to add suffix l/L with long datatypes.
        System.out.println(data3);


        byte num1 = 10;
        byte num2 = 5;

        // byte dataSum = num1 + num2; will throw an error to get and expression we have to use int only.

        int dataSum = num1 + num2;

        System.out.println(dataSum);


        // Real Numbers data types are float and double, for float we have to use f as suffix to make it work.


        float datar1 = 34.67f;

        double datar2 = 4677.89;

        System.out.println(datar1);
        System.out.println(datar2);

        //logical values there is boolean datatype.

        boolean datab = true;

        System.out.println(datab);


        // For characters we have a primitive data type char, It has to be within single quotes only //

        char dataC = 'a';

        System.out.println(dataC);





    }
    
}
